@dataclass(frozen=True)
class PropFamilyRecipe:
    """How a prop family is idiomatically sequenced, per the mined corpus."""

    family: str
    match_tokens: tuple[str, ...]
    effect_name: str
    # A name containing any of these tokens never matches, even when a
    # match_token hits — e.g. the mini-tree "tree" token must not claim
    # "TreeStar", "Mega Tree", or "Singing Tree" elements.
    exclude_tokens: tuple[str, ...] = ()
    # Alternate effect used on repeated sections (variation_seed odd) so
    # repeats vary, mirroring the Ripple/Shockwave alternation seen in the
    # corpus. None → always effect_name.
    alt_effect_name: str | None = None
    palette: tuple[str, ...] = ("#FFFFFF",)
    qualifying_labels: tuple[str, ...] = ("chorus", "drop", "hook", "climax", "build_peak")
    min_energy: int = 65
    # xLights parameter presets mined from the corpus placements. The primary
    # preset applies to effect_name; the alt preset applies to alt_effect_name
    # (empty → the alternate keeps library defaults).
    parameter_overrides: tuple[tuple[str, str], ...] = ()
    alt_parameter_overrides: tuple[tuple[str, str], ...] = ()
    # Optional secondary alternate keyed by section label rather than
    # variation_seed parity -- some families have a section label whose mined
    # idiom genuinely differs from both the primary and the seed-alternated
    # effect (e.g. arch bridges favor Spirals while arch choruses alternate
    # Single Strand/Shockwave -- see docs/arch_sequencing_corpus). Checked
    # before the seed-based alt. The label must also appear in
    # qualifying_labels, otherwise sections with that label never reach this
    # recipe in the first place.
    label_alt_labels: tuple[str, ...] = ()
    label_alt_effect_name: str | None = None
    label_alt_parameter_overrides: tuple[tuple[str, str], ...] = ()
    # Optional per-beat/per-occurrence direction rotation for the PRIMARY
    # effect's own preset (distinct from alt_effect_name, which swaps the
    # whole effect). Mined: genuine arch elements' Single Strand chase
    # direction splits ~evenly between Left-Right/Right-Left (alternating
    # per beat -- a "ping-pong" look) and a constant "Bounce from Right"
    # held for a whole section occurrence (see docs/arch_sequencing_corpus,
    # re-analyzed excluding the "Spinarchy" matrix/spinner false-positive
    # matches). variation_seed parity picks which style a given occurrence
    # gets. Empty direction_field -> no rotation; parameter_overrides' fixed
    # value is used for every beat, as before.
    direction_field: str | None = None
    direction_ping_pong_values: tuple[str, str] = ()
    direction_alt_value: str | None = None
    # Chase-size (Color_Mix1 -- percentage of lit pixels in the chase band)
    # rotation for the PRIMARY effect, paired with the same occurrence-style
    # parity bit as direction_field: about half the mined corpus songs hold
    # one band-size value for the whole song while the other half vary it
    # per section occurrence, always holding it constant across a whole
    # continuous run (never flickering per beat) -- the same idiom as
    # direction. size_values[0] is used with the primary (ping-pong)
    # direction style; size_values[1] with the alt (bounce) style. Empty
    # size_field -> no rotation; parameter_overrides' fixed value is used for
    # every beat, as before.
    size_field: str | None = None
    size_values: tuple[str, str] = ()
    # The reference packages place a section-spanning Off effect on the layer
    # beneath the beat bursts (xLights layer 2) on the group element, so the
    # props render black between bursts instead of picking up whole-house
    # bleed. Mined: snowflake Off placements are 43/58 on layer index 1;
    # arch 69/75 on layer index 1-2, spans of 12-15s tiling the sections.
    off_backdrop: bool = False
    # Optional third layer: a slower sustained motion effect rendered beneath
    # the per-beat primary (matrix idiom — the reference packages run the
    # matrices 2-3 motion layers deep under the On color layer, e.g.
    # On + Shockwave bursts + a continuous Spirals spin). Placed one segment
    # per ``secondary_beats_per_placement`` beats, solid white.
    secondary_effect_name: str | None = None
    secondary_parameter_overrides: tuple[tuple[str, str], ...] = ()
    secondary_beats_per_placement: int = 4
    # Ordered (effect_name, parameter_overrides) pairs cycled per qualifying
    # occurrence (variation_seed // 2 % len) in place of the two-effect
    # primary/alt alternation. Families with a wider mined motion vocabulary
    # (matrices) set this so repeated sections walk through every mined look
    # instead of ping-ponging between two. Empty () -> effect_name /
    # alt_effect_name behavior, unchanged.
    motion_rotation: tuple[tuple[str, tuple[tuple[str, str], ...]], ...] = ()
    # Beats per primary-motion segment. Most families place one segment per
    # beat (1); icicles run calmer 2-beat segments (mined medians 0.65-1.98s
    # against 0.44-0.65s beats — 1-4 beat segments, never per-beat bursts).
    beats_per_placement: int = 1
    # The On "2 is Unmask" color layer steps through the palette's vivid
    # colors one bar at a time instead of holding a single section-spanning
    # color. Both reference poles agree on this: On block medians are ~2.0s
    # and ~2.6s (= one bar), cycling blue→green→orange→... False → the
    # existing single section-spanning On.
    color_cycle_bars: bool = False
    # Two-layer "color over mask" composition, the dominant mega-tree idiom:
    # a section-spanning On on the upper layer with LayerMethod "2 is Unmask"
    # (517 mined placements), colored from the section theme, over the
    # recipe's motion effect on the layer directly below acting as the
    # brightness mask. Mined pairs: On-over-Spirals 269 placements,
    # On-over-Shockwave 214, On-over-Lightning 101, On-over-Pinwheel 52;
    # 72% of the tree's lit time runs 2+ layers deep.
    color_over_mask: bool = False


