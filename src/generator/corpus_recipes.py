def recipe_for_group(group: PowerGroup) -> PropFamilyRecipe | None:
    """Return the corpus recipe for a tier-6 PROP or tier-8 HERO group, or None.

    Tier 8 is included because a family's props don't always pair up into a
    tier-6 group — a layout with a single mega tree promotes it to
    ``08_HERO_Mega_Tree``, and the corpus effects were overwhelmingly placed
    on such individual model elements.

    Radial sub-groups (``prop_type == "radial"``) are never matched — they
    already have a dedicated chase-across-members placer that the corpus
    recipe must not displace.
    """
    if group.tier not in (6, 8) or group.prop_type == "radial":
        return None

    def _matches(recipe: PropFamilyRecipe, text: str) -> bool:
        return (
            any(token in text for token in recipe.match_tokens)
            and not any(token in text for token in recipe.exclude_tokens)
        )

    name = group.name.lower()
    for recipe in CORPUS_RECIPES:
        if _matches(recipe, name):
            # A generically named group ("06_PROP_Trees") can still hold a
            # different family's props ("Mega Tree 1/2") — when a majority of
            # members hit this recipe's exclude tokens, let the member-
            # majority pass below pick the right family instead.
            if group.members and recipe.exclude_tokens:
                excluded = sum(
                    1 for member in group.members
                    if any(t in member.lower() for t in recipe.exclude_tokens)
                )
                if excluded / len(group.members) > 0.5:
                    continue
            return recipe
    if group.members:
        for recipe in CORPUS_RECIPES:
            hits = sum(
                1 for member in group.members if _matches(recipe, member.lower())
            )
            if hits / len(group.members) > 0.5:
                return recipe
    return None


def section_qualifies(recipe: PropFamilyRecipe, section: SectionEnergy) -> bool:
    """Corpus recipes fire on chorus-like sections or high-energy passages."""
    label = (section.label or "").lower()
    if any(keyword in label for keyword in recipe.qualifying_labels):
        return True
    return section.energy_score >= recipe.min_energy
