r"""Corpus-derived prop-family placement recipes for tier-6 PROP groups.

Mined from 12 professionally hand-built reference packages (.xsqz) in a
local corpus folder (never committed) — see ``docs/snowflake_sequencing_corpus/``
and ``docs/arch_sequencing_corpus/``, generated by ``scripts/mine_prop_corpus.py``:

- **Snowflakes** — dominant group effect is Shockwave (66% of 3.4k group
  placements; SingleStrand 28%), solid white (93% white-base), one placement
  per beat (median 0.97 beats in the strongest exemplars, 0.5-1 beat
  corpus-wide), concentrated in choruses/high-energy passages.
  One corpus song alternates Ripple/Shockwave between sections.
- **Arches** — solid-white Single Strand chase, one segment per beat,
  chorus/outro-confined, optionally recolored by a mask layer (not replicated
  here in v1).
- **Mega trees** — Shockwave bursts (36% of tree-proper placements, same
  center-out preset as snowflakes) alternating with sustained single-spiral
  Spirals spins (18%), solid white dominant. See
  ``docs/megatree_sequencing_corpus/``.
- **Candy canes** — white SingleStrand chase (45% of 3.0k placements,
  ~half-beat segments) alternating with flat 2D Spirals (24%, ~one beat),
  colored by a section-spanning On layer with "2 is Unmask" over the motion
  effect (the mega-tree ``color_over_mask`` idiom; 459/467 On placements).
  See ``docs/cane_sequencing_corpus/``.
- **Matrices** — three layers deep: On "2 is Unmask" color over per-beat
  white Shockwave bursts (alt: mined Pinwheel) over a sustained Spirals
  spin, mirroring the corpus's multi-layer matrix stacks.
  See ``docs/matrix_sequencing_corpus/``.
- **Mini trees** — white Right-Left SingleStrand chase across the tree group
  (73% of 7.2k placements, ~0.6-beat segments), colored by the same
  On "2 is Unmask" layer, alternating with white Shockwave bursts.
  See ``docs/minitree_sequencing_corpus/``.
- **Horizontal / vertical lines** — white From-Head SingleStrand chase, one
  segment per beat (70%/66% of 1.6k/1.8k placements, 85-89% From Head),
  colored by the same On "2 is Unmask" layer (100% of On placements in both
  families), with white Lightning flickers as the recurring alternate
  (11%/18%, one unanimous preset). See
  ``docs/horizontal_sequencing_corpus/`` and
  ``docs/vertical_sequencing_corpus/``.

Snowflake and arch groups additionally carry a section-spanning **Off
backdrop** on the xLights layer beneath the bursts (``off_backdrop``): the
reference packages tile qualifying sections with 12-15s Off blocks on layer
index 1-2 of the group element so the props stay black between bursts.

A recipe only fires on qualifying sections (chorus-like label or high energy);
everywhere else the normal rotation/pool placement applies, so verses keep
today's behavior.

Matching is by name token on the tier-6 group name (``06_PROP_Snowflake``) or,
failing that, on a majority of member model names. Token matching is
substring-based ("arch" also matches "Arches"); tokens are chosen to be
distinctive within typical xLights prop naming.
"""

from __future__ import annotations

