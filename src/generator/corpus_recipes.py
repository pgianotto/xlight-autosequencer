"""Corpus-derived prop-family placement recipes for tier-6 PROP groups.

Mined from the 12 professionally hand-built reference packages (.xsqz) in
``F:\ShowFolderAI`` — see ``docs/snowflake_sequencing_corpus/`` and
``docs/arch_sequencing_corpus/``, generated by ``scripts/mine_prop_corpus.py``:

- **Snowflakes** — dominant group effect is Shockwave (66% of 3.4k group
  placements; SingleStrand 28%), solid white (93% white-base), one placement
  per beat (median 0.97 beats in the strongest exemplars, 0.5-1 beat
  corpus-wide), concentrated in choruses/high-energy passages.
  ``Gone Gone Gone`` alternates Ripple/Shockwave between sections.
- **Arches** — solid-white Single Strand chase, one segment per beat,
  chorus/outro-confined, optionally recolored by a mask layer (not replicated
  here in v1).

A recipe only fires on qualifying sections (chorus-like label or high energy);
everywhere else the normal rotation/pool placement applies, so verses keep
today's behavior.

Matching is by name token on the tier-6 group name (``06_PROP_Snowflake``) or,
failing that, on a majority of member model names. Token matching is
substring-based ("arch" also matches "Arches"); tokens are chosen to be
distinctive within typical xLights prop naming.
"""

from __future__ import annotations

from dataclasses import dataclass

from src.generator.models import SectionEnergy
from src.grouper.grouper import PowerGroup


@dataclass(frozen=True)
class PropFamilyRecipe:
    """How a prop family is idiomatically sequenced, per the mined corpus."""

    family: str
    match_tokens: tuple[str, ...]
    effect_name: str
    # Alternate effect used on repeated sections (variation_seed odd) so
    # repeats vary, mirroring the Ripple/Shockwave alternation seen in the
    # corpus. None → always effect_name.
    alt_effect_name: str | None = None
    palette: tuple[str, ...] = ("#FFFFFF",)
    qualifying_labels: tuple[str, ...] = ("chorus", "drop", "hook", "climax", "build_peak")
    min_energy: int = 65
    # xLights parameter preset mined from the corpus placements, applied to
    # the primary effect only (the alternate keeps library defaults — its
    # parameter space differs).
    parameter_overrides: tuple[tuple[str, str], ...] = ()


# Mined presets — near-unanimous across all 12 reference packages:
#
# SingleStrand chase (3.5k/4.9k placements use From Head; Color_Mix1 is the
# only per-song variable, 10-50, median 25): head-bright chase with a fading
# tail, the signature "comet" look of the reference shows.
_CHASE_FROM_HEAD: tuple[tuple[str, str], ...] = (
    ("E_NOTEBOOK_SSEFFECT_TYPE", "Chase"),
    ("E_CHOICE_Chase_Type1", "Left-Right"),
    ("E_CHOICE_Fade_Type", "From Head"),
    ("E_CHOICE_SingleStrand_Colors", "Palette"),
    ("E_SLIDER_Color_Mix1", "25"),
    ("E_SLIDER_Number_Chases", "1"),
)

# Shockwave burst (2.9k placements, fields >98% identical): center-out
# expanding ring, blended edges, single cycle.
_SHOCKWAVE_BURST: tuple[tuple[str, str], ...] = (
    ("E_NOTEBOOK_Shockwave", "Position"),
    ("E_CHECKBOX_Shockwave_Blend_Edges", "1"),
    ("E_CHECKBOX_Shockwave_Scale", "1"),
    ("E_SLIDER_Shockwave_Accel", "0"),
    ("E_SLIDER_Shockwave_CenterX", "50"),
    ("E_SLIDER_Shockwave_CenterY", "50"),
    ("E_SLIDER_Shockwave_Cycles", "1"),
    ("E_SLIDER_Shockwave_Start_Radius", "1"),
    ("E_SLIDER_Shockwave_Start_Width", "5"),
    ("E_SLIDER_Shockwave_End_Radius", "100"),
    ("E_SLIDER_Shockwave_End_Width", "35"),
)


CORPUS_RECIPES: tuple[PropFamilyRecipe, ...] = (
    PropFamilyRecipe(
        family="snowflake",
        match_tokens=("snowflake", "flake"),
        effect_name="Shockwave",
        alt_effect_name="Ripple",
        parameter_overrides=_SHOCKWAVE_BURST,
    ),
    PropFamilyRecipe(
        family="arch",
        match_tokens=("arch",),
        effect_name="Single Strand",
        parameter_overrides=_CHASE_FROM_HEAD,
    ),
)


def recipe_for_group(group: PowerGroup) -> PropFamilyRecipe | None:
    """Return the corpus recipe for a tier-6 PROP group, or None.

    Radial sub-groups (``prop_type == "radial"``) are never matched — they
    already have a dedicated chase-across-members placer that the corpus
    recipe must not displace.
    """
    if group.tier != 6 or group.prop_type == "radial":
        return None
    name = group.name.lower()
    for recipe in CORPUS_RECIPES:
        if any(token in name for token in recipe.match_tokens):
            return recipe
    if group.members:
        for recipe in CORPUS_RECIPES:
            hits = sum(
                1 for member in group.members
                if any(token in member.lower() for token in recipe.match_tokens)
            )
            if hits / len(group.members) > 0.5:
                return recipe
    return None


def section_qualifies(recipe: PropFamilyRecipe, section: SectionEnergy) -> bool:
    """Corpus recipes fire on chorus-like sections or high-energy passages."""
    label = (section.label or "").lower()
    if any(keyword in label for keyword in recipe.qualifying_labels):
        return True
    return section.energy_score >= recipe.min_energy
